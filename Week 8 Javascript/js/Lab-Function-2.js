//telt op
function addition (number1, number2) {
	return number1 + number2
}
document.write("10 + 12 = " + addition(10,12));
document.write("<br>")

//trekt af
function substract (number1, number2) {
	return number1 - number2;
}
document.write("58 - 34 = " + substract(58,34));
document.write("<br>")

//vermedigvuldigt
function multiplication (number1, number2) {
	return number1 * number2
}
document.write("6 x 7 = " + multiplication(6,7));
document.write("<br>")

//deelt door
function division (number1, number2) {
	return number1 / number2
}
document.write("144 : 12 = " + division(144,12));
document.write("<br>")

//telt op met 1
function increment (number1) {
	return number1 + 1
}
document.write("12 + 1 = " + increment(12));
document.write("<br>")

//trekt af met 1
function decrement (number1) {
	return number1 - 1
}
document.write("34 - 1 = " + decrement(34));
